
public class MilKilometreTablosu {
    public static void main(String[] args) {

        double MIL_TO_KM = 1.609;

        System.out.println("Mil --> Kilometre");
        System.out.println("-----------------");

        System.out.println("1" + " = " + (1 * MIL_TO_KM));
        System.out.println("5" + " = " +  (5 * MIL_TO_KM));
        System.out.println("10" + " = " +  (10 * MIL_TO_KM));
        System.out.println("20" + " = " +  (20 * MIL_TO_KM));
        System.out.println("50" + " = " +  (50 * MIL_TO_KM));
    }
}